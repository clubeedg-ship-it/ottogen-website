// Current language
let currentLang = localStorage.getItem('oopuo-lang') || detectBrowserLang();

function detectBrowserLang() {
  const lang = (navigator.language || navigator.userLanguage || 'en').substring(0, 2).toLowerCase();
  return translations[lang] ? lang : 'en';
}

// Geo-detect language on first visit (no localStorage set yet)
function geoDetectLang() {
  if (localStorage.getItem('oopuo-lang')) return; // user already chose
  
  fetch('https://ipapi.co/json/', { signal: AbortSignal.timeout(3000) })
    .then(function(r) { return r.json(); })
    .then(function(data) {
      if (!data || !data.country_code) return;
      var countryToLang = {
        NL: 'nl', BE: 'nl', SR: 'nl',
        BR: 'pt', PT: 'pt', AO: 'pt', MZ: 'pt',
        DE: 'de', AT: 'de', CH: 'de', LI: 'de',
        FR: 'fr', MC: 'fr', LU: 'fr', SN: 'fr', CI: 'fr', CM: 'fr',
        ES: 'es', MX: 'es', AR: 'es', CO: 'es', CL: 'es', PE: 'es', VE: 'es', EC: 'es', UY: 'es', PY: 'es', BO: 'es', CR: 'es', PA: 'es', DO: 'es', GT: 'es', HN: 'es', SV: 'es', NI: 'es', CU: 'es'
      };
      var detected = countryToLang[data.country_code];
      if (detected && translations[detected] && detected !== currentLang) {
        setLanguage(detected);
      }
    })
    .catch(function() { /* silent fail — keep browser lang */ });
}

// Run geo-detection after page loads
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', geoDetectLang);
} else {
  geoDetectLang();
}

function setLanguage(lang) {
  if (!translations[lang]) lang = 'en';
  currentLang = lang;
  localStorage.setItem('oopuo-lang', lang);
  applyTranslations();
  updateLangSwitcher();
}

function t(key) {
  return translations[currentLang]?.[key] || translations.en[key] || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else if (el.tagName === 'OPTION') {
        el.textContent = val;
      } else {
        el.textContent = val;
      }
    }
  });
  // Update HTML lang attr
  document.documentElement.lang = currentLang;
}

function updateLangSwitcher() {
  const langNames = { en:'EN', nl:'NL', pt:'PT', de:'DE', fr:'FR', es:'ES' };
  document.querySelectorAll('.lang-switcher button').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });
  // Update dropdown display
  const currentLabel = document.querySelector('.lang-current');
  if (currentLabel) currentLabel.textContent = langNames[currentLang] || 'EN';
  document.querySelectorAll('.lang-option').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });
}

function initI18n() {
  // Bind lang switcher buttons
  document.querySelectorAll('.lang-switcher button').forEach(btn => {
    btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
  });
  // Apply on load
  applyTranslations();
  updateLangSwitcher();
}

// Auto-init when DOM ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initI18n);
} else {
  initI18n();
}

// Language Dropdown Controller
document.addEventListener('DOMContentLoaded', function() {
  var dropdown = document.querySelector('.lang-dropdown');
  var toggle = document.getElementById('langToggle');
  var menu = document.getElementById('langMenu');
  var currentLabel = dropdown ? dropdown.querySelector('.lang-current') : null;
  
  if (!toggle || !menu) return;
  
  var langNames = { en:'EN', nl:'NL', pt:'PT', de:'DE', fr:'FR', es:'ES' };
  var lastTap = 0;
  
  // Set current language display
  if (currentLabel) currentLabel.textContent = langNames[currentLang] || 'EN';
  
  // Mark active on load
  var options = menu.querySelectorAll('.lang-option');
  for (var i = 0; i < options.length; i++) {
    if (options[i].dataset.lang === currentLang) {
      options[i].classList.add('active');
    } else {
      options[i].classList.remove('active');
    }
  }

  // Debounce helper — prevents touchend+click double-fire
  function debounced(fn) {
    return function(e) {
      var now = Date.now();
      if (now - lastTap < 300) return;
      lastTap = now;
      fn(e);
    };
  }

  // Toggle dropdown
  function doToggle(e) {
    if (e.type === 'touchend') e.preventDefault();
    e.stopPropagation();
    dropdown.classList.toggle('open');
  }
  toggle.addEventListener('touchend', debounced(doToggle), { passive: false });
  toggle.addEventListener('click', debounced(doToggle));
  
  // Language selection
  for (var j = 0; j < options.length; j++) {
    (function(btn) {
      function doSelect(e) {
        if (e.type === 'touchend') e.preventDefault();
        e.stopPropagation();
        
        var lang = btn.getAttribute('data-lang');
        if (!lang) return;
        
        // Update active state
        for (var k = 0; k < options.length; k++) options[k].classList.remove('active');
        btn.classList.add('active');
        
        // Update toggle display
        if (currentLabel) currentLabel.textContent = langNames[lang] || lang.toUpperCase();
        
        // Apply translation
        setLanguage(lang);
        
        // Close dropdown
        dropdown.classList.remove('open');
      }
      btn.addEventListener('touchend', debounced(doSelect), { passive: false });
      btn.addEventListener('click', debounced(doSelect));
    })(options[j]);
  }
  
  // Close on outside tap/click
  document.addEventListener('click', function(e) {
    if (dropdown && !dropdown.contains(e.target)) dropdown.classList.remove('open');
  });
});
