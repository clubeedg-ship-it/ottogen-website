var LANG_NAMES = { en:'EN', nl:'NL', pt:'PT', de:'DE', fr:'FR', es:'ES' };
var currentLang = localStorage.getItem('oopuo-lang') || detectBrowserLang();

function detectBrowserLang() {
  var lang = (navigator.language || navigator.userLanguage || 'en').substring(0, 2).toLowerCase();
  return translations[lang] ? lang : 'en';
}

function setLanguage(lang) {
  if (!translations[lang]) lang = 'en';
  currentLang = lang;
  localStorage.setItem('oopuo-lang', lang);
  applyTranslations();
  updateDropdown();
}

function t(key) {
  return (translations[currentLang] && translations[currentLang][key]) || translations.en[key] || key;
}

function applyTranslations() {
  var els = document.querySelectorAll('[data-i18n]');
  for (var i = 0; i < els.length; i++) {
    var el = els[i];
    var key = el.getAttribute('data-i18n');
    var val = t(key);
    if (val) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.textContent = val;
      }
    }
  }
  document.documentElement.lang = currentLang;
}

function updateDropdown() {
  var label = document.querySelector('.lang-current');
  if (label) label.textContent = LANG_NAMES[currentLang] || 'EN';
  var options = document.querySelectorAll('.lang-option');
  for (var i = 0; i < options.length; i++) {
    if (options[i].getAttribute('data-lang') === currentLang) {
      options[i].classList.add('active');
    } else {
      options[i].classList.remove('active');
    }
  }
}

// ============================================
// GEO DETECTION (first visit only)
// ============================================
function geoDetectLang() {
  if (localStorage.getItem('oopuo-lang')) return;

  fetch('https://ipapi.co/json/', { signal: AbortSignal.timeout(3000) })
    .then(function(r) { return r.json(); })
    .then(function(data) {
      if (!data || !data.country_code) return;
      var map = {
        NL:'nl', BE:'nl', SR:'nl',
        BR:'pt', PT:'pt', AO:'pt', MZ:'pt',
        DE:'de', AT:'de', CH:'de', LI:'de',
        FR:'fr', MC:'fr', LU:'fr',
        ES:'es', MX:'es', AR:'es', CO:'es', CL:'es', PE:'es', VE:'es'
      };
      var detected = map[data.country_code];
      if (detected && translations[detected] && detected !== currentLang) {
        setLanguage(detected);
      }
    })
    .catch(function() {});
}

// ============================================
// DROPDOWN CONTROLLER
// ============================================
function initDropdown() {
  var dropdown = document.querySelector('.lang-dropdown');
  var toggle = document.getElementById('langToggle');
  var menu = document.getElementById('langMenu');

  if (!toggle || !menu || !dropdown) return;

  var lastTap = 0;

  function handleToggle(e) {
    var now = Date.now();
    if (now - lastTap < 400) return;
    lastTap = now;
    if (e.type === 'touchstart') e.preventDefault();
    e.stopPropagation();
    dropdown.classList.toggle('open');
  }

  // Use touchstart instead of touchend — more reliable on mobile
  toggle.addEventListener('touchstart', handleToggle, { passive: false });
  toggle.addEventListener('click', handleToggle);

  var options = menu.querySelectorAll('.lang-option');
  for (var j = 0; j < options.length; j++) {
    (function(btn) {
      function handleSelect(e) {
        var now = Date.now();
        if (now - lastTap < 400) return;
        lastTap = now;
        if (e.type === 'touchstart') e.preventDefault();
        e.stopPropagation();

        var lang = btn.getAttribute('data-lang');
        if (!lang || !translations[lang]) return;

        setLanguage(lang);
        dropdown.classList.remove('open');
      }
      btn.addEventListener('touchstart', handleSelect, { passive: false });
      btn.addEventListener('click', handleSelect);
    })(options[j]);
  }

  // Close on outside tap
  document.addEventListener('touchstart', function(e) {
    if (!dropdown.contains(e.target)) dropdown.classList.remove('open');
  });
  document.addEventListener('click', function(e) {
    if (!dropdown.contains(e.target)) dropdown.classList.remove('open');
  });
}

// ============================================
// INIT
// ============================================
function initI18n() {
  applyTranslations();
  updateDropdown();
  initDropdown();
  geoDetectLang();
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initI18n);
} else {
  initI18n();
}
